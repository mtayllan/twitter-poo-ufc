    private void preencheDados(){
        try {
            perfil = myTwitter.getUsuario(usuarioLogado);
            if (perfil instanceof PessoaJuridica){
                idTextField.setText(String.valueOf(((PessoaJuridica) perfil).getCnpj()));
            }else{
                idTextField.setText(String.valueOf(((PessoaFisica) perfil).getCpf()));
            }
            chkIsAtivo.setSelected(perfil.isAtivo());
        } catch (PIException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }

