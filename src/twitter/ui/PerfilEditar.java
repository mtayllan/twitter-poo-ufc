    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (perfil instanceof PessoaFisica){
                PessoaFisica pessoaFisica = new PessoaFisica(usuarioLogado);
                pessoaFisica.setAtivo(chkIsAtivo.isSelected());
                pessoaFisica.setCpf(Long.parseLong(idTextField.getText()));
                myTwitter.atualizarPerfil(pessoaFisica);
            }else{
                PessoaJuridica pessoaJuridica = new PessoaJuridica(usuarioLogado);
                pessoaJuridica.setAtivo(chkIsAtivo.isSelected());
                pessoaJuridica.setCnpj(Long.parseLong(idTextField.getText()));
                myTwitter.atualizarPerfil(pessoaJuridica);
            }
        } catch (PIException | PDException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

